        <div class="col-md-6">
          <!-- <h3>Our Commitment</h3> -->
          <p><span class="intro text-uppercase">Within prisons</span>, our vision is to change food systems so that all prisoners can better access healthier food, to support their physical and mental health so aiding rehabilitation. Improving mental and physical health is one of the nine key factors known to reduce reoffending. We work alongside catering and food procurement staff, providing consultancy and training.  We work directly with prisoners running healthy eating courses, cookery workshops and training peer supporter ‘Food Champions’.</p>
          <p><span class="intro text-uppercase">At national level</span>, we work with government departments and suppliers to help embed the provision of healthier food choices for prisoners within policy.</p>
          <p><span class="intro text-uppercase">In the community</span>, we aim to equip those serving community sentences or recently released from prison with food-related life skills to help them to survive and flourish in a life without crime. Our courses and workshops cover essential life skills such as cooking, shopping and even social eating. We can target courses to specific groups such as young offenders, parents or people recovering from substance misuse. We can also train offender managers and volunteers, to help them offer support around making healthier food choices.</p>
          <p><span class="intro text-uppercase">Our courses</span> are participatory, inclusive and fun. We use motivational techniques to gently guide participants towards change. We include discussion, problem-solving and reflective learning, and our sessions always involve tasting of healthy snack alternatives.</p>
          <p class="text-dark"><em>If you work with offenders in prisons or the community, please <a href="#" data-toggle="modal" data-target="#myModal" id="white-bg-link">contact us</a> to find out more.</em></p>
